void testar_gerar_chave_publica(llint p, llint q, llint e, llint n) {
  assert(gerar_chave_publica(p, q, e) == n);
}

void testar_encriptar(const char *msg, size_t tamanho_msg, llint n, llint e, const llint msg_cripto[]) {
  llint msg_gerada[tamanho_msg];

  encriptar(msg, tamanho_msg, n, e, msg_gerada);
  assert(!memcmp(msg_gerada, msg_cripto, tamanho_msg * sizeof(*msg_cripto)));
}

void testar_desencriptar(
  const llint msg_cripto[], size_t tamanho_msg, llint p, llint q, llint e, const char *msg
) {
  char msg_gerada[tamanho_msg + 1];

  desencriptar(msg_cripto, tamanho_msg, p, q, e, msg_gerada);
  msg_gerada[tamanho_msg] = '\0';
  assert(!strcmp(msg, msg_gerada));
}

void testar_tudo(const char *msg, size_t tamanho_msg, llint p, llint q, llint e) {
  llint msg_cripto_gerada[tamanho_msg], n;
  char msg_gerada[tamanho_msg + 1];

  n = gerar_chave_publica(p, q, e);
  assert(n != 0);
  encriptar(msg, tamanho_msg, n, e, msg_cripto_gerada);
  desencriptar(msg_cripto_gerada, tamanho_msg, p, q, e, msg_gerada);
  msg_gerada[tamanho_msg] = '\0';
  assert(!strcmp(msg, msg_gerada));
}

